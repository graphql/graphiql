export default function(objects) {
  const sortedObject = objects.concat();
  sortedObject.sort((a, b) => {
    if (a.name < b.name) {
      return -1;
    }
    if (a.name > b.name) {
      return 1;
    }
    return 0;
  });
  return sortedObject;
}
